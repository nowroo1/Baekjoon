a = int(input())


def number(n, k):
    if k == n-1:
        return 1
    elif k == n-2:
        return 2
    elif k == 1:
        return n-1
    else:
        return number(n-1, k-1) * 2


for i in range(a):
    a, b = map(int, input().split())

    print(number(a, b))

